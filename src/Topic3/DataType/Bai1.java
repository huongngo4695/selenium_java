package Topic3.DataType;

public class Bai1 {
    static int a;
    static int b;

    public static void main(String[] args) {
        a=6;
        b=2;
        System.out.println("a+b=" + (a+b));
        System.out.println("a-b=" + (a-b));
        System.out.println("a*b=" + (a*b));
        System.out.println("a/b=" + (a/b));
    }
}
