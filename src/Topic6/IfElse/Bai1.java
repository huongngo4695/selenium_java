package Topic6.IfElse;

public class Bai1 {
    public static void timsochan(int n) {
        if (n % 2 == 0) {
            System.out.println("n là số chẵn");
        } else {
            System.out.println("n là số lẻ");
        }
    }
    public static void main(String[] args) {
        timsochan(5);
    }
}
